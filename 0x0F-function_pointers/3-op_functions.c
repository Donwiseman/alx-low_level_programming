/**
 * op_add - add the two int arguments passed
 * @a: first int parameter
 * @b: second int parameter
 *
 * Return: returns the addition of the arguments
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - add the two int arguments passed
 * @a: first int parameter
 * @b: second int parameter
 *
 * Return: returns the subtraction of the arguments
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - add the two int arguments passed
 * @a: first int parameter
 * @b: second int parameter
 *
 * Return: returns the multiplication of the arguments
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - add the two int arguments passed
 * @a: first int parameter
 * @b: second int parameter
 *
 * Return: returns the division of the arguments
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - add the two int arguments passed
 * @a: first int parameter
 * @b: second int parameter
 *
 * Return: returns the modulus of the arguments
 */
int op_mod(int a, int b)
{
	return (a % b);
}
